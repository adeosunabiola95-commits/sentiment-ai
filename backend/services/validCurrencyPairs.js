export function isValidCurrencyPair(pair) {
  if (typeof pair !== "string" || pair.length !== 6) return false;
  const base = pair.slice(0, 3);
  const quote = pair.slice(3, 6);
  if (base === quote) return false;
  return SUPPORTED_CODES.has(base) && SUPPORTED_CODES.has(quote);
}
